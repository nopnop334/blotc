<script>
    const scrollOffset = 300;

    const scrollElements = document.querySelectorAll(".list");
    const elementInView = (el, offset = 0) => {
        const elementTop = el.getBoundingClientRect().top;
        return (
            elementTop <=
            ((document.documentElement.clientHeight) - offset)
        );
    };

    const displayScrollElement = (el) => {
        let ani_in_img = $('.image', el).attr('data-ani_in');
        let ani_in_text = $('.box', el).attr('data-ani_in');
        let ani_out_img = $('.image', el).attr('data-ani_out');
        let ani_out_text = $('.box', el).attr('data-ani_out');
        $('.image', el).addClass(ani_in_img)
        $('.box', el).addClass(ani_in_text)
        $('.image', el).removeClass(ani_out_img)
        $('.box', el).removeClass(ani_out_text)
        //scrollElement.classList.add('scrolled');
    }

    const hideScrollElement = (el) => {
        //scrollElement.classList.remove('scrolled');
        let ani_in_img = $('.image', el).attr('data-ani_in');
        let ani_in_text = $('.box', el).attr('data-ani_in');
        let ani_out_img = $('.image', el).attr('data-ani_out');
        let ani_out_text = $('.box', el).attr('data-ani_out');
        $('.image', el).removeClass(ani_in_img)
        $('.box', el).removeClass(ani_in_text)
        $('.image', el).addClass(ani_out_img)
        $('.box', el).addClass(ani_out_text)
    }

    const handleScrollAnimation = () => {
        scrollElements.forEach((el) => {
            if (elementInView(el, scrollOffset)) {
                displayScrollElement(el);
            } else {
                hideScrollElement(el);
            }
        })

    }

    window.addEventListener('scroll', () => {
        handleScrollAnimation();
        cloud_move();
    })

    function cloud_move() {
        let elementTop = $(window).scrollTop()
        //console.log(elementTop);
        let c1_ = '-' + (elementTop / 10) + '%';
        let c2_ = (elementTop / 10) + '%';
        $('.banner_content .cloud_1').css({
            "-webkit-transform": "translate(" + c1_ + ",0)"
        })
        $('.banner_content .cloud_2').css({
            "-webkit-transform": "translate(" + c2_ + ",0)"
        })
        if ((elementTop) > 300) {
            $('.banner_content .scroll_down').addClass('hide')
        } else {
            $('.banner_content .scroll_down').removeClass('hide')
        }
    }
</script>

